import math

pi = -3.14

print(round(pi)) # round the float value
print(math.ceil(pi)) # find ceiling
print(math.floor(pi)) # find floor 
print(abs(pi)) # find absolute value
print(pow(5,2)) # find power of a number
print(math.sqrt(25)) # find square root of a number
print(max(100,200,300)) # find maximum
print(min(100,200,300)) # find minimum