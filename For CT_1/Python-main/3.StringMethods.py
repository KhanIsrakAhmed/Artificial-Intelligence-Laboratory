name = "mahbubur rahman"
print(name)
print(len(name))  # Find length
print(name.find("b"))  # return the first index of the character
print(name.capitalize())  # capitalize the string
print(name.upper())  # uppercase the string
print(name.lower())  # lowercase the string
print(name.isdigit())  # check the string is digit or not
print(name.isalpha())  # check the string contains only alphabet or not
print(name.count("b"))  # / count frequency of the character from string
print(name.replace("a", "x"))  # replace the character from string
print(name*3)  # print same thing multiple time

